#!/usr/bin/env python3
"""
generate_rpc.py — Generate Rust RPC code from rpc_protocol.toml

Produces:
  - rpc_common_generated.rs  (constants)
  - rpc_target_generated.rs  (target-side dispatch)
  - rpc_host_generated.rs    (host-side client callers)

Usage:
  python3 generate_rpc.py rpc_protocol.toml --out-dir src/
"""

import argparse
import os
import sys
from pathlib import Path

try:
    import tomllib
except ImportError:
    try:
        import tomli as tomllib
    except ImportError:
        print("ERROR: need tomllib (Python 3.11+) or tomli", file=sys.stderr)
        sys.exit(1)


# ---------------------------------------------------------------------------
# Type → hex digit count + encoder/decoder helpers
# ---------------------------------------------------------------------------

TYPE_INFO = {
    "u8":       {"digits": 2,  "enc": "add_u32_no_padding", "dec": "next_u8"},
    "u16":      {"digits": 4,  "enc": "add_u32_le",         "dec": "next_u16"},
    "u16_be":   {"digits": 4,  "enc": "add_u32",            "dec": "next_u16_be"},
    "u32":      {"digits": 8,  "enc": "add_u32_le",         "dec": "next_u32"},
    "u32_le":   {"digits": 8,  "enc": "add_u32_le",         "dec": "next_u32"},
    "u32_be":   {"digits": 8,  "enc": "add_u32",            "dec": "next_u32_be"},
    "bool":     {"digits": 1,  "enc": "add_u8",             "dec": "next_bool"},
}

# Reverse mapping: class id byte → class name
CLASS_ID_TO_NAME = {}


def load_spec(path):
    """Load and validate the TOML spec."""
    with open(path, "rb") as f:
        data = tomllib.load(f)

    # TOML dotted keys like [class.GEN] create nested dicts:
    # data["class"]["GEN"]["command"]["START"] etc.
    classes_data = data.get("class", {})
    classes = []
    for class_name, class_val in classes_data.items():
        class_id = class_val["id"]
        handler = class_val.get("handler", f"rpc_{class_name.lower()}_impl")
        description = class_val.get("description", "")

        commands_data = class_val.get("command", {})
        commands = []
        for cmd_name, cmd_val in commands_data.items():
            cmd_id = cmd_val["id"]
            cmd_desc = cmd_val.get("description", "")
            params = cmd_val.get("params", [])
            reply = cmd_val.get("reply", "ok")

            old_name = cmd_val.get("old_name", None)
            commands.append({
                "name": cmd_name,
                "id": cmd_id,
                "description": cmd_desc,
                "params": params,
                "reply": reply,
                "old_name": old_name,
            })

        classes.append({
            "name": class_name,
            "id": class_id,
            "handler": handler,
            "description": description,
            "commands": commands,
        })
        CLASS_ID_TO_NAME[class_id] = class_name

    return classes


def const_name(class_name, cmd_name=None):
    """Generate the constant name for a class or command."""
    if cmd_name:
        return f"RPC_{class_name}_{cmd_name}"
    return f"RPC_{class_name}_PACKET"


def rust_byte_literal(c):
    """Convert a single ASCII character to a Rust byte literal."""
    if c.isascii() and c.isprintable() and c not in "'\\":
        return f"b'{c}'"
    return f"{ord(c)}u8"


# ---------------------------------------------------------------------------
# Generator: rpc_common_generated.rs
# ---------------------------------------------------------------------------

def gen_common(classes):
    lines = [
        "// Auto-generated by generate_rpc.py — DO NOT EDIT",
        "// Source: rpc_protocol.toml",
        "",
        "#![allow(dead_code)]",
        "#![allow(non_upper_case_globals)]",
        "#![allow(unused_variables)]",
        "#![allow(unused_imports)]",
        "",
        "// ── Packet class bytes ──",
    ]
    for cls in classes:
        cname = const_name(cls['name'])
        lines.append(f"pub const {cname}: u8 = {rust_byte_literal(cls['id'])};")

    lines += ["", "// ── Command bytes ──"]
    for cls in classes:
        for cmd in cls["commands"]:
            cname = const_name(cls['name'], cmd['name'])
            lines.append(f"pub const {cname}: u8 = {rust_byte_literal(cmd['id'])};")

    lines += ["", "// ── Response codes ──",
              "pub const RPC_RESP_OK: u8 = b'K';",
              "pub const RPC_RESP_PARERR: u8 = b'P';",
              "pub const RPC_RESP_ERR: u8 = b'E';",
              "pub const RPC_RESP_NOTSUP: u8 = b'N';",
              "pub const RPC_REMOTE_RESP: u8 = b'&';",
              "",
              "// ── Error codes ──",
              "pub const RPC_ERROR_UNRECOGNISED: u8 = 1;",
              "pub const RPC_ERROR_WRONGLEN: u8 = 2;",
              "pub const RPC_ERROR_FAULT: u8 = 3;",
              "pub const RPC_ERROR_EXCEPTION: u8 = 4;",
              "",
              "// ── Framing ──",
              "pub const RPC_END: u8 = b'#';",
              ]

    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Generator: rpc_target_generated.rs
# ---------------------------------------------------------------------------

def gen_target(classes):
    lines = [
        "// Auto-generated by generate_rpc.py — DO NOT EDIT",
        "// Source: rpc_protocol.toml",
        "",
        "#![allow(dead_code)]",
        "#![allow(non_snake_case)]",
        "#![allow(unused_variables)]",
        "#![allow(unused_imports)]",
        "",
        "use crate::rpc_common_generated::*;",
        "use crate::rpc_target::rpc_parser::rpc_parameter_parser;",
        "use crate::rpc_target::*;",
        "use crate::rpc_target::rpc_reply::*;",
        "use crate::bmp;",
        "use crate::parsing_util;",
        "use crate::encoder::*;",
        "",
        "crate::setup_log!(false);",
        "crate::gdb_print_init!();",
        "",
        "// ── Top-level dispatch ──",
        "#[unsafe(no_mangle)]",
        "pub fn rpc_dispatch(input: &[u8]) -> bool {",
        "    if input.len() < 2 {",
        "        bmplog!(\"**short rpc\\n\");",
        "        return false;",
        "    }",
        "    let mut parser = rpc_parameter_parser::new(input);",
        "    match parser.next_cmd() {",
    ]

    for cls in classes:
        if not cls["commands"]:
            continue
        lines.append(f"        {const_name(cls['name'])} => {cls['handler']}::dispatch(&mut parser),")

    lines += [
        "        _ => {",
        "            bmplog!(\"wrong RPC header\\n\");",
        "            false",
        "        }",
        "    }",
        "}",
        "",
    ]

    # Generate per-class dispatch modules
    for cls in classes:
        if not cls["commands"]:
            continue
        lines += [f"// ── {cls['name']} dispatch ──",
                  f"pub mod {cls['handler']} {{",
                  f"    use super::*;",
                  f"",
                  f"    pub fn dispatch(parser: &mut rpc_parameter_parser) -> bool {{",
                  f"        match parser.next_cmd() {{",
                  ]

        for cmd in cls["commands"]:
            cname = const_name(cls["name"], cmd["name"])
            lines.append(f"            {cname} => cmd_{cmd['name'].lower()}(parser),")

        lines += ["            _ => {",
                   "                bmplog!(\"unknown command in {} packet\\n\", stringify!(" + cls["name"] + "));",
                   "                false",
                   "            }",
                   "        }",
                   "    }",
                   ""]

        # Generate per-command handler functions
        for cmd in cls["commands"]:
            fn_name = f"cmd_{cmd['name'].lower()}"
            lines.append(f"    fn {fn_name}(parser: &mut rpc_parameter_parser) -> bool {{")

            # Decode params
            for p in cmd["params"]:
                ptype = p["type"]
                pname = p["name"]
                if ptype == "hex_block":
                    lines.append(f"        let {pname} = parser.end();")
                elif ptype == "bool":
                    lines.append(f"        let {pname} = parser.next_cmd() != b'0';")
                else:
                    ti = TYPE_INFO[ptype]
                    lines.append(f"        let {pname} = parser.{ti['dec']}();")

            # Call impl function
            impl_fn = cmd["name"].lower()
            param_names = [p["name"] for p in cmd["params"] if p["type"] != "hex_block"]
            hex_block_params = [p["name"] for p in cmd["params"] if p["type"] == "hex_block"]

            # Build the impl call
            impl_args = ", ".join(param_names)
            if hex_block_params:
                if impl_args:
                    impl_args += ", "
                impl_args += ", ".join(hex_block_params)

            reply = cmd["reply"]
            if isinstance(reply, str):
                if reply == "ok":
                    lines.append(f"        crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append("        rpc_reply_encoder::new().end();")
                    lines.append("        true")
                elif reply == "string":
                    lines.append(f"        let s = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append("        rpc_reply_string(s);")
                    lines.append("        true")
                else:
                    # Simple type reply
                    lines.append(f"        let result = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append(f"        rpc_reply32_le_ok(result);")
                    lines.append("        true")
            elif isinstance(reply, dict):
                rtype = reply.get("type", "ok")
                on_error = reply.get("on_error", None)
                if rtype == "string":
                    lines.append(f"        let s = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append("        rpc_reply_string(s);")
                    lines.append("        true")
                elif rtype == "compound":
                    # Compound reply with multiple fields
                    fields = reply.get("fields", [])
                    field_names = [f["name"] for f in fields]
                    field_types = [f["type"] for f in fields]
                    # Call impl, get tuple back
                    field_list = ", ".join(field_names)
                    lines.append(f"        let ({field_list}) = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    if on_error == "fault":
                        lines.append(f"        if {field_names[0] if 'fault' not in field_names else 'fault'} != 0 {{")
                        lines.append("            rpc_reply_error(RPC_ERROR_FAULT);")
                        lines.append("            return true;")
                        lines.append("        }")
                    lines.append("        let mut reply = rpc_reply_encoder::new();")
                    for i, f in enumerate(fields):
                        ft = f["type"]
                        if ft == "u32_le":
                            lines.append(f"        reply.add_u32_le_wrapped({field_names[i]});")
                        elif ft == "u8":
                            lines.append(f"        reply.add_u8_wrapped({field_names[i]} as u8);")
                        else:
                            lines.append(f"        reply.add_u32_le_wrapped({field_names[i]});")
                    lines.append("        reply.end();")
                    lines.append("        true")
                elif rtype == "hex_block":
                    max_len = reply.get("max_length", 1024)
                    lines.append(f"        let buffer = crate::rpc_target::rpc_reply::get_temp_buffer();")
                    # Add buffer param to impl args
                    buffer_impl_args = impl_args
                    if buffer_impl_args:
                        buffer_impl_args += ", "
                    buffer_impl_args += "buffer"
                    lines.append(f"        let (fault, len) = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({buffer_impl_args});")
                    if on_error == "fault":
                        lines.append("        if fault != 0 {")
                        lines.append("            rpc_reply_error(fault as u8);")
                        lines.append("            return true;")
                        lines.append("        }")
                    lines.append(f"        rpc_reply_hex_string(RPC_RESP_OK, &buffer[..len]);")
                    lines.append("        true")
                elif rtype == "u8":
                    lines.append(f"        let val = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append(f"        #[allow(clippy::unnecessary_cast)]")
                    lines.append(f"        rpc_reply_ok(val as u8);")
                    lines.append("        true")
                elif on_error == "ok_bool":
                    lines.append(f"        let (ok, value) = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append("        rpc_reply_bool_32le(ok, value);")
                    lines.append("        true")
                elif on_error == "fault":
                    lines.append(f"        let (fault, value) = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append("        reply_adiv5_32(fault, value);")
                    lines.append("        true")
                else:
                    lines.append(f"        let value = crate::rpc_target_impl::{cls['handler']}::{impl_fn}({impl_args});")
                    lines.append(f"        rpc_reply32_le_ok(value);")
                    lines.append("        true")
            lines.append("    }")
            lines.append("")

        lines.append("}\n")

    return "\n".join(lines) + "\n"


